# To access all math Functions
from math import*

# To print Number
print("Number = ", 102)

# To print string with number
age = 25
print("My age is "+str(age))

# Find Highest Number
number = 1, 3, 4, 5, -4, -13, 23, 875
print("Maximum Number = ", max(number))

# Find Minimum Number
print("Minimum Number = ", min(number))

# Calculate Power of Number
num = 5
print("Power = ", pow(num, 2))

# Round off the Number
print("Rounded Number = ", round(52/36+pow(num, 2)))

# Absolute Value
print("Absolute Value = ", abs(-round(52/36+pow(num, 2))))

# Calculating Square root
print("Square root = ", sqrt(num))

# ceil and floor functions
x = 23.51
y = 77.5
print("Ceil Value = ", ceil(x))
print("Floor Value = ", floor(y))

# Calculating Factorial
print("Factorial of "+str(num)+" = ", factorial(num))
